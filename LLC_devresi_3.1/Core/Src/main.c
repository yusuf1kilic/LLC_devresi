void pageProcess(int page){
	switch(page){

	
		case 0:
				lcd_clear();
				lcd_put_cur(0,0);
				lcd_send_string("isleme hazir 1");
			break;
		case 1:
				lcd_clear();
				lcd_put_cur(0,0);
				lcd_send_string("isleme hazir 2");
			break;
		case 2:
				lcd_clear();
				lcd_put_cur(0,0);
				lcd_send_string("isleme hazir 3");
			break;
		case 3:
				lcd_clear();
				lcd_put_cur(0,0);
				lcd_send_string("isleme hazir 4");
			break;
		case 4:
				lcd_clear();
				lcd_put_cur(0,0);
				lcd_send_string("isleme hazir 5");
			break;
		default:
			lcd_clear();	
			lcd_put_cur(0,0);
			lcd_send_string("sayfa hatasi");
			
			
	}
	
		while(1){
		
			if(HAL_GPIO_ReadPin(GPIOB,GPIO_PIN_6)==1){
				lcd_clear();
				lcd_put_cur(0,0);
				lcd_send_string("Cikis yapildi");
				menuControl();
		}
	
	}
	
}

void menuControl(void){
	while(1){
		if(HAL_GPIO_ReadPin(GPIOB,GPIO_PIN_3)==1){
			changePage = (changePage + 1) % TOTAL_PAGES;
			pageName(changePage);
			HAL_Delay(100);
		
		}else if(HAL_GPIO_ReadPin(GPIOB,GPIO_PIN_4)==1){
		
			if (changePage == 0) {
				changePage = TOTAL_PAGES - 1;
				pageName(changePage);
			}else{
				changePage = changePage - 1;
				pageName(changePage);
				
			}
			HAL_Delay(100);
		
		}else if(HAL_GPIO_ReadPin(GPIOB,GPIO_PIN_5)==1){
			
				pageProcess(changePage);
			
		
		}
	
	}
	
}

